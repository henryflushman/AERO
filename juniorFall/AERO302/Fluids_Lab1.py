diam = 1.65 # meters
radius = diam/2

def mirror_tb(A):
    A = np.asarray(A, dtype=float)
    return np.vstack((np.flipud(A), A))

V_matrix = [V_d1, V_d2, V_d3, V_d4, V_d5, V_d6]
for i in range(6):
    V_matrix[i] = mirror_tb(V_matrix[i])

# Global color scale for comparability
vmin = min(np.nanmin(A) for A in V_matrix)
vmax = max(np.nanmax(A) for A in V_matrix)

# Figure: 2 rows for plots, thin 3rd row for the colorbar
